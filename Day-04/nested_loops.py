# Rectangle (5 Rows × 2 Columns)
print("Rectangle (5 Rows × 2 Columns)")
for i in range(5):
    for j in range(2):
        print("*", end="")
    print()

# Square (4 × 4)
print("\nSquare (4 × 4)")
for i in range(4):
    for j in range(4):
        print("*", end="")
    print()

# Rectangle (5 Rows × 3 Columns)
print("\nRectangle (5 Rows × 3 Columns)")
for i in range(5):
    for j in range(3):
        print("*", end="")
    print()

# Rectangle (3 Rows × 6 Columns)
print("\nRectangle (3 Rows × 6 Columns)")
for i in range(3):
    for j in range(6):
        print("*", end="")
    print()

# Understanding Rows and Columns
print("\nRows and Columns Example (3 × 5)")
for i in range(3):
    for j in range(5):
        print("*", end="")
    print()
