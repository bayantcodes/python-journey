# AND Operator
bank_balance = 5000
pin = True

if bank_balance >= 5000 and pin == True:
    print("Transaction Successful")


# OR Operator
shopping_price = 1000
premium_member = True

if shopping_price >= 1000 or premium_member == True:
    print("Discount Applied")


# NOT Operator
wifi = False

if not wifi:
    print("Connect to WiFi")
